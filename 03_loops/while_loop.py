# While Loop in Python
# ==========================================

# Example 1: Print numbers from 1 to 5

count = 1

while count <= 5:
    print(count)
    count += 1

print("----------------------------------")

# Example 2: Countdown

number = 5

while number > 0:
    print(number)
    number -= 1

print("Blast Off!")