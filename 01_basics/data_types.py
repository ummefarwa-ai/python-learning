# ==========================================
# Data Types in Python
# ==========================================

# Integer (int)
age = 27
print("Integer:", age)
print(type(age))

print("----------------------------------")

# Float (float)
height = 5.4
print("Float:", height)
print(type(height))

print("----------------------------------")

# String (str)
name = "Umme Farwa"
print("String:", name)
print(type(name))

print("----------------------------------")

# Boolean (bool)
is_student = True
print("Boolean:", is_student)
print(type(is_student))

print("----------------------------------")

# None Type
result = None
print("None:", result)
print(type(result))

print("----------------------------------")

# Type Conversion

number = "100"

print(type(number))

number = int(number)

print(number)
print(type(number))

print("----------------------------------")

price = 99

price = float(price)

print(price)
print(type(price))

print("----------------------------------")

value = 25

value = str(value)

print(value)
print(type(value))