# ==========================================
# Variables in Python
# ==========================================

# Basic variables
name = "Umme Farwa"
age = 27
height = 5.4
is_student = True

print(name)
print(age)
print(height)
print(is_student)

print("----------------------------------")

# Multiple assignment
x, y, z = 10, 20, 30

print(x)
print(y)
print(z)

print("----------------------------------")

# Assigning multiple string values
fruit1, fruit2, fruit3 = "Apple", "Banana", "Cherry"

print(fruit1)
print(fruit2)
print(fruit3)

print("----------------------------------")

# Swapping variables
a = 5
b = 10

a, b = b, a

print(a)
print(b)